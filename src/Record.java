public class Record {

	//properties
	public String name ="";
	public String id = "";
	public double pay = 0;
	
	//Single Constructor
	public Record(String name, String id, double pay){
		this.name = name;
		this.id = id;
		this.pay = pay;
	} //end of constructor
	
	
	
	
} //
